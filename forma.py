from tkinter import *
window = Tk()
window.title('Графика')
window.geometry('800x600')
canvas = Canvas(window, width = 800, height=600,background='#7109AA')
canvas.pack(expand=True,fill=BOTH)
canvas.create_line(100,100,700,100,width=10,fill='#ffffff')
canvas.create_line(100,110,700,110,width=10,fill='blue')
canvas.create_line(100,120,700,120,width=10,fill='red')
canvas.create_oval(200,200,300,300,width=1,fill='red')
canvas.create_rectangle(400,200,500,300,width=3,fill='green',outline='orange')
canvas.create_text(450,250,text='квадрат',fill='purple')
canvas.create_polygon(200,350,225,400,275,400,240,450,250,500,200,460,150,500,160,450,125,400,175,400,200,350)
window.mainloop()